// If Statements example
if (age >= 18) {
  console.log("You’re eligible to vote!");
}

// Else Statements example
if (age >= 18) {
  console.log("You’re eligible to vote!");
} else {
  console.log("You’re not old enough to vote.");
}

// Else If Statements example
if (score > 80) {
  console.log("Great job!");
} else if (score > 50) {
  console.log("Not bad, keep trying!");
} else {
  console.log("Needs improvement.");
}

// Ternary Operators example
const message = age >= 18 ? "You’re eligible to vote!" : "You’re not old enough to vote.";

